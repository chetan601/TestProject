
public class SwitchFile
{
	public static void main(String [] args)
	{
		int a=2;
		switch(a)
		{
		case 1: System.out.println("Monday");
		     break;
		case 2: System.out.println("Tuesday");
	     break;
		case 3: System.out.println("Wednesday");
	     break;
		case 4: System.out.println("Sunday");
	     break;
		}
	}

}
